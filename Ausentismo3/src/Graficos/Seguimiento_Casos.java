    /**
     * Este método se usa para llenar la tabla en seguimiento con los examenes
     * médicos de ingreso
     */
    private void inicializarTablaEI(){
        ArrayList<ausentismo.ExamenDeIngreso> ExIngreso = this.ExamenesIngreso;
        modeloTabla = new DefaultTableModel();
        modeloTabla.addColumn("FECHA REGISTRO");
        modeloTabla.addColumn("CEDULA");
        modeloTabla.addColumn("NOMBRE");
        modeloTabla.addColumn("APELLIDOS");
        //modeloTabla.addColumn("ESTADO");
        modeloTabla.addColumn("DIAGNOSTICO");
        Object[] iniciar = {"","","","",""};
        int tam = (ExIngreso.size()<28)? 28 : ExIngreso.size();
        for (int i = 0; i < tam; i++) {
            if(i<ExIngreso.size()){                
                ausentismo.ExamenDeIngreso E = ExIngreso.get(i);
                Object[] datosTabla = {E.getFecha_Registro(),E.getCC(),E.getNombre(),E.getApellido(),E.getDiagnostico()};
                modeloTabla.addRow(datosTabla);
            }else{
                modeloTabla.addRow(iniciar);
            }
        }
        TablaCasos.setModel(modeloTabla);
    }

