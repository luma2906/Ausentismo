    private void btnNuevoSeguimiento1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoSeguimiento1ActionPerformed
        if(TablaCasos.getSelectedRow()>=0){ //Se comprueba que se haya seleccionado algun item de la tabla
            String id_Seguimiento = datosTabla.get(TablaCasos.getSelectedRow())[0];
            String estado = datosTabla.get(TablaCasos.getSelectedRow())[4];
            System.out.println("ESTADOOOOOO "+estado);
            if(estado.toUpperCase().equals("ABIERTO")){ // Se comprueba que el caso que se va a cerrar se encuentre abierto
                BaseDatos.Seguimiento_BD.cerrarCaso(id_Seguimiento);
                inicializarTablaCasos();
                datosTabla.get(TablaCasos.getSelectedRow())[4] = "CERRADO";
            }else{
                JOptionPane.showMessageDialog(null, "No puede cerrar un caso que ya se encuentra cerrado.","CASO CERRADO",JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Por favor seleccione un caso a cerrar","Error",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnNuevoSeguimiento1ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int index = TablaCasos.getSelectedRow();
        if(index>=0){
            if(index < this.datosTabla.size()){

                Caso ventana = new Caso(null,true);
                ventana.setLocationRelativeTo(null);  
                if(radioButtonCasos.isSelected()){
                    ventana.setDatosIniciales(this.datosTabla.get(index));
                    
                }else{
                    ventana.setDatosInicialesEI(convEItoString(this.ExamenesIngreso.get(index)));
                    
                }
                ventana.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Por favor seleccione un registro que contenga datos.", "Error!!!", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Por favor seleccione un caso de la tabla.", "Error!!!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton6ActionPerformed
    private String[] convEItoString(ExamenDeIngreso EI){
        String[] datos;
        datos = new String[9];
        datos[0]=EI.getID_Examen(); 
        datos[1]=EI.getFecha_Registro().toString();
        datos[2]=EI.getCC();
        datos[3]=EI.getNombre();
        datos[4]="ABIERTO";
        datos[5]=EI.getDiagnostico();
        datos[6]="";
        datos[7]=EI.getApellido();
        datos[8]="1";
        return datos;
    }
