    private void txtCCKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCCKeyPressed
        if(evt.getKeyCode()==KeyEvent.VK_ENTER){ //Si se presiona enter en la caja de texto de cc
            BaseDatos.Demografia_BD dem = new BaseDatos.Demografia_BD();
            ausentismo.ModeloTablaEmpleado emp = dem.getTrabajador(Integer.parseInt(txtCC.getText()));
            if(emp != null){
                lblNombre.setText(emp.getValueAt(0, 1)+" "+emp.getValueAt(0, 2));//Nombre y apellidn
                btnAceptar.setEnabled(true);
                this.cc = txtCC.getText();
            }else{
                JOptionPane.showMessageDialog(null, "La cedula escrita no corresponde a ningun empleado","Error",JOptionPane.ERROR_MESSAGE);
                lblNombre.setText("Por favor digite la cedula y presione enter.");
                btnAceptar.setEnabled(false);
            }
        }
    }//GEN-LAST:event_txtCCKeyPressed

