    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        switch (listExamenes.getSelectedIndex()) {
            case 0: //Examenes de ingreso
                
                break;
            case 1: //Examenes periodicos
                RegistrarExamenPeriodico exm = new RegistrarExamenPeriodico(null,true);
                exm.setNombreCompleto(lblNombre.getText());
                exm.setCc(this.cc);
                exm.setVisible(true);
                this.dispose();
                break;
            case 2: // Recomendaciones de citas medicas
                
                break;
            default:
                JOptionPane.showMessageDialog(null,"Error inesperado","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

